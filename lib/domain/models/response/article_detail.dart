class ArticleDetailEntity {
  final String id;
  final String title;
  final String url;
  final String image;
  final String src;
  final String content;
  final String author;
  final String time;

  ArticleDetailEntity({
    required this.id,
    required this.title,
    required this.url,
    required this.image,
    required this.src,
    required this.content,
    required this.author,
    required this.time,
  });
}
